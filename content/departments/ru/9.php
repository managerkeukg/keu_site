<DIV class="headers">КАФЕДРА ИНОСТРАННЫХ ЯЗЫКОВ</DIV>
   
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2000 году на базе кафедры кыргызского и русского языков была сформирована кафедра иностранных языков. И. о. зав. кафедрой была назначена ст. преподаватель Садирова И. М. С 2001 по настоящее время кафедру возглавляет профессор, отличник образования Исабекова Б. А. Штат кафедры состоит из 16 сотрудников: заведующая кафедрой, профессор; 4 доцента; 10 старших преподавателей; 1 преподаватель и 1 методист.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        История кафедры тесно связана со становлением университета. Коллектив кафедры пополняется специалистами, среди которых немало высококвалифицированных преподавателей, побывавших на стажировках в зарубежных странах, в частности, в Англии, Турции, США, Шотландии, Чехии, Франции, Германии и Индии.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        На кафедре преподаются 4 предмета: английский, немецкий, французский, китайский языки. Кафедра на должном уровне обеспечена современными учебниками и учебными пособиями как отечественных, так и зарубежных авторов. Это методические пособия, Оксфордские и Кембриджские инновационные аудио-видео курсы в трех уровнях. А также используется аудио-видео курс по французскому языку «Le francais a grande vitesse», по немецкому языку- бизнес курс «Deutsch im Hotel», составление ситуативных диалогов, проигрывание малых спектаклей. Для осуществления задачи повышения качества обучения укрепляется материально-техническая база: оборудованы кабинеты, студенты обеспечены необходимыми современными учебными аудио-видео материалами и учебно-методическими комплексами, разработанными преподавателями кафедры. На кафедре изданы учебники и учебные пособия для студентов, изучающих иностранные языки:
   <UL class="ul_blue">
	    <LI>- «Кыргызско-русско-английский словарь по сельскому хозяйству» Исабекова Б.А. (есть авторское свидетельство);
        <LI>- «Кыргызско-русско-английский словарь экономических и финансовых терминов», Исабекова Б.А. (есть авторское свидетельство);
	    <LI>- Учебник английского языка по разви-тию речи “English in use”, Бегельдинова А.Х., 2015;
	    <LI>- Английский язык. Программа, методические указания и контрольные задания для студентов дистантного обучения по специальности «Налогообложение», Бегельдинова А.Х., Субанбекова К.А., 2007;

	    <LI>- Методические указания и контрольные задания для факультета Менеджмента и туризма заочного отделения, Исбекова Б.А. Садирова И.М., Асанова Д.К.- 2007;  <LI>- Методические разработки для студентов дистантного обучения по специальности Бухучет, Финансы и кредит, составители: проф. Исабекова Б.А., ст. преп. адирова И.М., Субанбекова К.А., Кожомкулова Г.Э.- 2007
	    <LI>- Kyrgyzstan is my native land – Убалиева М.У.- 2008;
	    <LI>- Actual problems of tourism development - Убалиева М.У.- 2011;
	    <LI>- «English for students of Economics and Tourism», составители: доцент Убалиева М.У., ст. преп. Жолдошбекова Э.А., преп. Кулубаева А . М., 2013;
	    <LI>- «Английский язык для студентов экономистов и магистрантов» по направлению «Экономика», составители: Аминова Б.И., Алымкулова Г.Ж. 2012 ;
	    <LI>- Омурзакова К.А. – «Учебное пособие по немецкому языку по направлению Туризм» - 2014.
     </UL>
   </DIV>

