<DIV class="headers">КАФЕДРА ПРИКЛАДНОЙ ИНФОРМАТИКИ</DIV>

<DIV class="image_right" ><IMG SRC="img/113.jpg"></IMG></DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        История кафедры начинается с 1996 г, когда по инициативе руководителя вуза проф. Камчыбекова Т.К. была организована кафедра высшей математики и информатики. Основной задачей кафедры была применение современных средств вычислительной техники в учебном процессе по подготовке кадров экономических специальностей. Заведующим новой кафедры был назначен к.ф.-м.н, доцент Токтакунов Т. На начальном этапе на кафедре работали известные ученые - доктор физико-математических наук, лауреат Государственной премии Кыргызской Республики Искандаров С.И., доктор физико-математических наук Бийбосунов Б.И., профессор Сулайманов К.С., кандидаты физико-математических наук, доценты Токтакунов Т, Осмонова Ч., Раманкулов С.Т., Сабитов Б.Р. и др.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2001 году в целях улучшения и централизации обслуживания учебного процесса факультетов института для более профессиональной координации стратегической политики вуза по широкомасштабному применению компьютерной техники, как в учебном процессе, так и в управлении учебным процессом, на базе кафедры высшей математики и информатики были созданы кафедры прикладной информатики и математических и естественно-научных дисциплин. Заведующим вновь образованной кафедры прикладной информатики был назначен к.ф.-м.н., доцент Токтакунов Т.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В последующие годы кафедру возглавляли к.ф.-м.н., Асанов Т., к.т.н. Керимкулов Т., которые внесли определенный вклад в развитии кафедры.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Кафедра выиграла грант Фонда «СОРОС-Кыргызстан», на средства которого компьютерные классы вуза были бесплатно подключены к глобальной сети Internet.
   </DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Преподаватели кафедры активно участвуют во всех международных программах, реализуемых в университете, активно сотрудничают по линии образовательной сети Ednet.
   </DIV>
