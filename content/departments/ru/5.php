 <DIV class="text_title">Учебная работа</DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Информационные технологии во всех областях человеческой деятельности существенно расширяют возможности, поэтому резко возрастает потребность в кадрах, обладающих соответствующими знаниями и навыками. В настоящее время специалисты в области информационных технологий в экономике являются наиболее востребованной профессией не только в нашей республике, но и в странах СНГ.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Достаточно глубокие знания действительных возможностей сегодняшних компьютерных технологий и умение пользоваться этими возможностями нужны всем нашим выпускникам. Работа в этом направлении на кафедре ведется комплексно. Учебные занятия по информационным технологиям проводятся в классах, оснащенных современной компьютерной техникой, подключенной к внутренней локальной сети, и внешней сети Internet по беспроводной технологии Wi-Fi и по высокоскоростному оптоволоконному кабелю.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В целях приобщения студентов к новым информационным технологиям, в частности, ознакомления с современными при-кладными программами в области финансов и кредита, бухгалтерского учета и т.д. на старших курсах по блоку вузовских компонентов читаются курсы «Автоматизированные информационные технологии в экономике», «1С Бухгалтерия», «Информационные системы реинжиниринга бизнеспроцессов» и др.
   </DIV>

 <DIV class="text_title">Учебно-методическая работа</DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
       На кафедре учебно-методическая работа ведется согласно ранее разработанной стратегии по направлениям:
   <UL class="ul_blue">
	    <LI>- выработка комплексного научно–методического подхода к решению проблемы информатизации обучения в рамках реализуемых государственных образовательных стандартов для специальностей университета;
        <LI>- материально-техническое оснащение университета, вопросы внутренних и внешних информационных коммуникаций;
	    <LI>- наработка методик применения новыхи нформационных технологий;
	    <LI>- анализ эффективности внедрения информационных технологий обучения.
     </UL>

   </DIV>

 <DIV class="image_center" ><IMG SRC="img/115.jpg"></IMG></DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        На кафедре ведется подготовка учебников, учебных пособий, учебно-методической литературы по информатике и информационным технологиям, создан банк лекционных материалов и лабораторных заданий.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Разработаны электронные учебники по «Информатике и ИКТ», «Имитационное моделирование экономических систем», новые лабораторные работы по дисциплинам «Информационные системы реинжиниринга бизнес-процессов на ARIS ToolSet», «Компьютерные технологии управления проектами на MS Project-2010» и др. Преподавателями также подготовлены и изданы объемные учебные пособия, учебно-методические комплексы по компьютерной графике и другим основным дисциплинам.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Преподавателями кафедры за последние годы изданы: «Лекции по проектированию информационных систем» (авторы к.ф.-м.н. Токтакунов Т., к.т.н. Керимкулов Т.), учебно-методическое пособие «1С-Бухгалтерия» - (авторы к.ф.-м.н. Токтакунов Т., к.т.н. Керимкулов Т., Усенова К.Дж.), учебно-методическое руководство «Практические задания по электронной таблице Excel-XP» (авторы к.ф.-м.н. Токтакунов Т., к.т.н. Керимкулов Т.,Усенова К.Дж.), учебное пособие «Компьютерные технологии управления проектами» (авторы к.ф.-м.н. Токтакунов Т., к.ф.-м.н. Баячорова Б. и др.), лабораторные работы по языку программирования Delphi-7 (авторы к.ф.-м.н. Токтакунов Т., старший преподаватель Касмалиев Т.К.), учебно-методические работы старших преподавателей Кулушовой А., Аманалиевой М., Кулуевой Ф., а также электронные УМК и силлабусы преподавателей по всем читаемым предметам, размещенные в образовательном портале КЭУ.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Учебник кандидата педагогических наук Касымалиева М.У. «Информатика. Базовый курс» и задачник к этому учебнику для учащихся 7-9 классов изданы с грифом Министерства образования и науки тиражом 130 тысяч экземпляров в первом издании, и в 2006 г. 135 тысяч экземпляров - во втором.
   </DIV>

