 <DIV class="text_title">Научно-исследовательская работа</DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Научно-исследовательская работа на кафедре ведется по направлению «Математическое моделирование экономико-экологических систем» совместно с Институтом геомеханики и освоения недр НАН Кыргызской Республики по теме «Методы оценки устойчивости оползневой опасности горных склонов и мероприятия по снижению опасности и риска», а также по направлению «Теоретические основы методической системы обучения информатики и математики» совместно с Кыргызской академией образования по теме «Педагогика и преподавание информатики и математики в вузах и средних общеобразовательных школах»
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        По результатам научных исследований преподаватели кафедры активно выступают с докладами и научными сообщениями в республиканских и международных научных конференциях. За последние два года преподавателями опубликовано более 30 научных статей в Республиканских научных журналах - Вестник КЭУ им. М.Рыскулбекова, Вестник КНУ им. Ж.Баласагына, в вузовских сборниках научных конференций, а также в научной печати Российской Федерации, Казахстана и Польши на русском и английском языках.
   </DIV>

<DIV class="text_title">Международные связи кафедры</DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Сотрудники кафедры активно участвуют в проекте Tacis-Tempus «Туризм и менеджмент в гостиничном сервисе» и прошли стажировку в ведущих вузах и научных центрах Европы. Старший преподаватель Касымалиев М.У. проходил стажировку в Бельгии, в Высшей школе Эрасмуса. по применению информационных систем в гостиничном хозяйстве, а Токтакунов Т. стажировался в университете г.Турин (Италия) по программе Менеджмент в учебном процессе.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Преподаватели кафедры постоянно повышают свою квалификацию по линии образовательной сети EdNet.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Кафедра имеет договоры о сотрудничестве по обмену опытом и учебно-методическими материалами с вузами Казахстана, а также участвуют в научно-практических конференциях, преподаватели публикуют научные статьи в изданиях вузов ближнего и дальнего зарубежья.
   </DIV>

<DIV class="text_title">Перспективные направления работы кафедры</DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Успех развития нашего государства в XXI веке, его возможности выбирать и реализовывать оптимальную историческую траекторию в полной мере зависят от наличия современной образовательной и информационной сферы общества. Об этом убедительно свидетельствует то, что любые глобальные перемены, с которыми сталкивается общество и цивилизация в целом, неизбежно сказываются на состоянии сферы образования. Учитывая это, можно утверждать, что стратегические цели, пути и этапы развития компьютерной технологии обучения в нашем университете совпадают с общими направлениями информатизации общества в целом.		
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

<DIV class="image_center" ><IMG SRC="img/118.jpg"></IMG></DIV>

   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Так, на современном этапе идет переход распространения научно-технической и экономико-коммерческой информации на бумажном носителе (журналы, препринты, научно-технические отчеты) к распространению этой информации в электронной форме и на техническом носителе через международные и национальные банки данных и компьютерные сети. 
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Поэтому перспективные направления работы кафедры по организации учебного процесса ориентированы по следующим трем основным направлениям:
   <UL class="ul_blue">
	    <LI>- обучение информационным технологиям в экономике как специфическому виду деятельности, т.е. как специальности;
        <LI>- обучение информационным технологиям как составной части деятельности каждого современного экономиста;
	    <LI>- расширение использования информационных технологий непосредственно в процессе обучения.
     </UL>
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Для достижения этого в университете принимаются соответствующие меры по качественному усилению роли информационного обеспечения учебного процесса по внедрении передовых достижений информационных технологий. Проделан большой объем работы эффективного использования информационных ресурсов Всемирной сети интернет, компьютерных сетей в информационном обеспечении учебного процесса университета. В рамках реализации этих планов силами кафедры разработаны и широко используется как преподавателями, так и студентами, Образовательный портал КЭУ по интернет-адресу keu.page.kg, где размещен богатейший банк данных учебно-методических материалов, разработанные преподавателями университета, а также большое количество полезных материалов и информационных ресурсов для студентов.
   </DIV>

