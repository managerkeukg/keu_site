   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Кафедра «Туризм, гостеприимство и предпринимательство» была создана в 1999 году. Кафедра осуществляет подготовку по направлениям «Менеджмент», «Туризм» и специальностям «Социально- культурный сервис и туризм» и «Менеджмент организации» (специализация «Менеджмент в гостиничном сервисе»). Состав кафедры укомплектован преподавателями, имеющими большой педагогический и производственный опыт. На кафедре работают д.э.н., профессор Атышов К.А., д.э.н., профессор Чубурова Ж.Т., к.э.н., профессор Алкадыров М.А., д.г.н., профессор Чонтоев Д.Т., доцент Шадыканова Г.У., к.э.н., доцент Расулова Н.К., опытные преподаватели Бейшеева А.Ж., Татышов У.К., Осмонжанова Ж.А. Долгие годы на кафедре работал Заслуженный экономист республики, доктор экономических наук, профессор Сыдыков К.С. Для усиления практической направленности преподавания на кафедру приглашаются ведущие специалисты сферы туризма и гостеприимства. Профессорско-преподавательский состав кафедры постоянно работает над повышением своей квалификации. Преподаватели Татышов У.К., Эгембердиева Д.Э., Турдугулова М.М., Осмонжанова Ж.А. закончили аспирантуру, Расулова Н.К. и Бейшеева А.Ж. закончили магистратуру. До 2002 года кафедрой заведовал д.э.н., профессор Атышов К.А. С 2002 года заведует кафедрой д.э.н., профессор Чубурова Ж.Т.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2006 году был заключен Меморандум о сотрудничестве КЭУ с Госагентством по туризму при правительстве КР, в соответствии с которым КЭУ является научно-учебной площадкой по подготовке кадров в сфере туризма, ныне подписан аналогичный Меморандум с Департаментом Туризма при Министерстве культуры, информации и туризма КР. В 2015 году подписан Меморандум сотрудничества с Кыргызской Ассоциацией Туроператоров (КАТО).
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Для прохождения практики студентами, заключаются договора с туристическими организациями и гостиницами, которые активно сотрудничают с КЭУ. Ведущие специалисты туристских и гостиничных организаций привлекаются для чтения лекций, руководства дипломными работами, участия в семинарах и конференциях, проводимых в КЭУ.
   </DIV>

<DIV class="text_title">Учебно-методическая работа  </DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        На кафедре формируется все необходимое для подготовки квалифицированных специалистов, соответствующих современным требованиям кыргызстанских и международных стандартов. Действуют On Line класс и учебно-научная лаборатория «Менеджмент и туризм».
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Предусмотренный учебным планом 600-часовой курс иностранного языка, а также членство в Евродип дают возможность студентам факультета, обучающимся по направлениям: «Туризм», Менеджмент» и специальностям: «Менеджмент организации» и «Социально-культурный сервис и туризм» продолжить образование в любом из европейских вузов.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Разрабатываются учебники и учебно-методические пособия с учетом требований Брюссельского (Бельгия), Салфордского университета (Великобритания), а также сегодняшних реалий индустрии туризма и гостеприимства Кыргызстана. Кафедра, при поддержке Фонда Евразия, осуществляла проект «Образовательное и информационное обеспечение туристической отрасли Кыргызстана», в рамках которого выпущены учебные пособия по курсам «Экотуризм», «Маркетинг в сфере туризма».
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Университет также был удостоен гранта Темпус по сетевому проекту «Изучение туризма и гостеприимства в Центральной Азии», где партнерами являются университеты Узбекистана, Монголии, Бельгии, Италии, Финляндии.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В целях повышения качества образования в учебном плане предусмотрено изучение второго иностранного языка. Курсы «Актуальные проблемы развития туризма и гостеприимства», «Транспортное обслуживание туристов» читаются на английском языке.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Особое внимание преподавателями кафедры уделяется разработке учебно-методических материалов. За последние годы были опубликованы более десятка учебников и учебных пособий. Основные из них:
   <UL class="ul_blue">
	    <LI>- Экотуризм (К.Атышов, Б.Турдумамбетов)
        <LI>- Маркетинг в сфере туризма (Т.Камчыбеков и др.)
	    <LI>- Экотуризм (Алкадыров М., Ажибеков Б.)
	    <LI>- Управление достопримечательностями (Расулова Н.К., Алымбеков.К.А)
        <LI>- Экономика природопользования (К.Сыдыков)
        <LI>- Кыргызстандын тышкы экономикасы (Атышов К.А.)
        <LI>- Природно-рекреационные ресурсы Кыргызстана и проблемы их рационального использования (Атышов К.А., Чонтоев Д.Т.)
        <LI>- Потенциал горного туризма Кыргызстана (Д.Т.Чонтоев).
     </UL>

   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Преподаватели большое внимание уделяют созданию электронных версий учебников и лекций по дисциплинам кафедры. Студенты специальностей кафедры проходят производственную практику в гостиницах и турфирмах г. Бишкек, а также, по соглашению сторон об обмене студентами, в Бельгии, Турции, Норвегии.
   </DIV>

<DIV class="text_title">Научно-исследовательская работа  </DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Тема научно-исследовательской работы кафедры: «Актуальные проблемы развития туризма и индустрии гостеприимства». 
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Результаты исследований публикуются в научных журналах, материалах научных конференций, монографиях. Преподаватели кафедры принимают активное участие в республиканских и международных конференциях.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Профессор Чубурова Ж.Т. является Лауреатом конкурса на лучшее эссе по экономике Института экономического развития Всемирного банка (1 премия), профессор Атышов К.А. в 2013 году стал победителем конкурса Фонда прогрессивных инициатив.
   </DIV>

 <DIV class="image_center" ><IMG SRC="img/103.jpg"></IMG></DIV>


