   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Студенты Алиев Т. и Бексултанова А. в рамках международного научного форума «Рыскуловские чтения» участвовали в конференции в Казахском экономическом университете им. Рыскулова и получили сертификаты (Алматы, 2012 год);
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Студентка Барыкова Е. участвовала в финале Международного конкурса научно-исследовательских проектов молодых ученых и студентов «Eurasia green» Евразийского экономического форума молодежи и заняла пятое место в номинации «Модель рационального природопользования» (г.Екатеринбург 2013 год).
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Студентки Токонова Н. и Касымбекова М. за участие в Международном конкурсе «Кухня без границ» в номинации «Лучшая научно-исследовательская работа» были награждены дипломом II степени (г. Екатеринбург, Россия, 2014г.)
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Студентка IV курса специальности «Социально-культурный сервис и туризм» Токонова Н. стала обладательницей Президентской стипендии (2014 г.) и участвовала в международном студенческом фестивале Theme ISFiT 2015: Corruption в Трондхейме (Норвегия, февраль 2015г.)
   </DIV>

<DIV class="text_title">Международные связи</DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Внешние связи и международное сотрудничество кафедры осуществляются в рамках договоров сотрудничества университета, подписанные с учебными заведениями, предприятиями, учреждениями и организациями города Бишкек Кыргызской Республики и зарубежных государств. 
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        С 1999 года кафедра участвовала в следующих международных проектах в области сферы туризма:
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Проект ТЕМПУС-ТАСИС CD 20583-1999 «Разработка учебной программы для специальности «Туризм гостеприимство»;
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Проект TEMPUSMPJEP 23027-2002 «Обучение туризму и гостеприимству в Центральной Азии»;
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Проект Европейской комиссии Темпус SCMTTER - Т003А06-2006 «Обеспечение качества»;
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Проект «Образование в области инновации и устойчивого туризма в Кыргызской Республике» (Норвегия, Грузия). (2013 г.);
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Проект «Усиление социального партнерства в профессиональном образовании Кыргызстана», реализуемый при поддержке ЕС (2013 г.).
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В рамках этих проектов прошли стажировку в Высшей школе Эрасмус (Брюссель, Бельгия) 11 преподавателей, Салфордском Университете (Манчестер, Англия) - 5 преподавателей, Норвегии - 3 преподавателя. Разработан и апробирован конвергентный учебный план по туризму и гостеприимству. Происходит постоянный обмен студентами и преподавателями с университетами зарубежных стран.
   </DIV>

<DIV class="text_title">Работа с выпускниками</DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Работа с выпускниками - одно из приоритетных направлений деятельности кафедры.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В современных условиях востребованность специалиста, его конкурентоспособность и возможность карьерного роста определяются соответствием их знаний современным требованиям.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Основными методами работы кафедры с выпускниками являются вовлечение их в студенческую жизнь посредством участия в различных мероприятиях: таких, как встреча с выпускниками, конференции, круглые столы, лекции, праздники КЭУ, «Дни открытых дверей», экскурсии и прохождение практики на предприятиях, где работают наши выпускники.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Многие их них работают по своей специальности: в министерствах, гостиницах и турфирмах Кыргызстана. Так, выпускница Бапиева Анара - менеджером в Pyx-Ордо, Усубалиев Максат - заместителем директора Департамента туризма министерства культуры, информации и туризма, Сакеева Альфия - менеджером ДОЦ «Маяк», Логай Анна и Жакыпбекова Мээрим - менеджерами в «Kyrgyz Concept», Гормолысева Юлия, Жолдошбекова Айпери - менеджерами по туризму в «Kyrgyz Travel Agency», Асанбекова Жамиля - в турфирме «Жайлоо тур» Нарынской области, Текеева Эльмира — администратором «Урмат-Ордо», Мадраимов Нурсултан - менеджером в гостинице «Silk Road», Дооронова Айчурок - менеджером в гостинице «Золотой Дракон», Эсеркамышева Дильбара - менеджером в гостинице «Урмат Ордо».
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Спектр применения выпускниками полученных знаний не ограничивается отечественными компаниями. В них заинтересованы и зарубежные компании, о чем свидетельствует количество выпускников, которые работают в таких странах, как Турция, Таиланд, Россия, Китай, Казахстан, Бельгия, Франция, Египет и т.д. Так, выпускница Нарматова Гулиза - отельный гид в «Пегас Туристик» (Египет), Шаршеналиев Абдысамат и Абдиева Анара - в «Пегас Туристик» (Таиланд), Антропкин Юрий - логист в «Logistic firm» (Ирландия), Амиралиев Нурбек и Сопуева Диана - в гостиницах Анталии (Турция) и др.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Выпускники-отличники в основном поступают в аспирантуру и магистратуру университета и работают преподавателями в КЭУ и других вузах. Эгембердиева Дильбара, Осмонжанова Жылдыз и Турдугулова Мира остались работать на кафедре. Выпускник Токталиев Талант организовал туристическую фирму «ITS Company», Муратов Азиз является Председателем комитета по делам молодежи, Абакиров Азамат обучается в докторантуре в Турции, Оторбаева Эльвира - в магистратуре во Франции.
   </DIV>

<DIV class="text_title">Достижения университета в подготовке кадров для сферы туризма</DIV>


   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2005 году КЭУ стал обладателем первой премии Республиканского конкурса «Лучший вуз по подготовке специалистов для туризма».
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2006 году – университет становится учебной и научно-производственной площадкой подготовки кадров для индустрии туризма Кыргызстана.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2006 году университет стал лауреатом фонда Европейского Гран-при за достигнутые высокие показатели качества в сфере высшего профессионального образования, проведенного на основе мониторинга качества подготовки специалистов.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2007 году – Гран-при Международной ярмарки «Туризм и путешествия BITF-2007».
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2012 году КЭУ прошел международную аккредитацию по направлениям «Экономика» и «Менеджмент».
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        В 2014 году КЭУ стал обладателем гран при Национальной премии Kyrgyz Tourism Awards в номинации «Лучшее учебное заведение по направлению туризма».
   </DIV>

