 <DIV class="text_title">История кафедры.</DIV>

   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Кафедра математики и естественнонаучных дисциплин возникла при разделении кафедры математики и информатики решением Ученого Совета Университета от 8 июля 2002 года. Ей была поручена организация преподавания математики и естественнонаучных дисциплин студентам, обучающимся по экономическим направлениям.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Заведование кафедрой было поручено к..ф-м.н., профессору Сулайманову К.С. С июня 2013 года руководство кафедрой осуществляет доцент Супаева Г.Т.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Главная задача работы коллектива кафедры - подготовка высококвалифицированных кадров, обладающих фундаментальными знаниями и современными компетенциями в сфере экономико-математического анализа и моделирования, статистической обработки данных, применения количественного инструментария обоснования управленческих решений. Исходя из этого, основными направлениями деятельности кафедры являются:

     <UL class="ul_blue">
	    <LI>- Совершенствование качества образовательного процесса.
        <LI>- Совершенствование профессионального уровня профессорско-преподавательского состава.
        <LI>- Развитие научно-исследовательской деятельности с ориентацией на современные проблемы.
        <LI>- Совершенствование внедрения в учебный процесс инновационных образовательных технологий, ориентированных на развитие личности и профессиональных качеств студентов. 
     </UL>

   </DIV>
 
 
