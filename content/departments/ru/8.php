   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Окуу-методикалык Кеңешмесинде кафедранын окутуучулары Б.Ж.Балтабаева, А.М.Калыбекова, А.Т.Убукеевалардын авторлугунун негизинде даярдалган «Учебное пособие по практическому курсу русского языка для студентов неязыковых факультетов вузов» аттуу окуу китеби, Г.Ж.Алиясова жана Б.К.Адылованын «Кыргыз тили. Тексттер жана тапшырмалар» аттуу жогорку окуу жайлардын 1-2 курстарына ылайыкталган методикалык колдонмосу бекитилип, басмаканадан чыгарылып, азыркы күндө пайдаланууда.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Андан сырткары кафедранын мүчөлөрү тарабынан төмөнкү эмгектер жарык көргөн: Жапаралиева Н.Ж, Адылова Б.К. «Иш кагаздарынын мамлекеттик тилде жүргүзүлүшү», «Т.Сыдыкбековдун өмүрү, чыгармачылыгын окутуунун инновациялык технологиялары» (монография); Г.А.Узбекова - «Краткий тематический словарь по русскому языку для студентов 1-2 курсов» жана «Риторика» боюнча окуу-методикалык комплекси; А.М.Калыбекова, С.Б.Ногойбаева - «Словарь по специальности».
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        2010-жылы «КРнын жогорку окуу жайлары үчүн компетенттүүлүк мамиленин негизинде эң мыкты инновациялык окуу-методикалык комплекси» сынагынын финалисти ага окутуучу С.Е.Ахметова жана 2011-жылы КРнын Билим берүү жана илим министрлиги, Борбордук Азиядагы билим берүү “EDNET”фонду тарабынан жарыяланган конкурска “Манаста-таануу” боюнча окуу-усулдук комплекси үчүн кафедранын доцентинин милдетин аткаруучу Б.А.Каримова жеңүүчү болушкан. 
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        2013-окуу жылы Кыргыз Республикасынын жогорку профессионалдык билим берүүсүнүн мамлекеттик билим берүү стандартынын «Экономика», «Коммерция», «Бизнес башкаруу», «Менеджмент» адистиктери боюнча үчүнчү муундагы стандарт кафедранын доценти Н.Ж.Жапаралиева тарабынан мамлекеттик тилге которулду.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Мамлекеттик жана расмий тилдер кафедрасы тарабынан жыл сайын университетте ар түрдүү илимий иш-чаралар уюштурулуп турат. Мисалы, «Мектеп-орто профессионалдык билим берүү мекемелери - ЖОЖ: мамлекеттик жана расмий тилдерди окутуунун көйгөйлөрү жана келечеги» деген темада 2013-жылдын 30-майында тегерек столду ийгиликтүү өткөрүп, модератордук ишти кафедранын доценттери А.Т.Убукеева, Г.А.Узбекова алып барышты.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Кафедра мүчөлөрү ЖОЖдордо болгон илимий –методикалык, илимий –практикалык конференцияларга активдүү катышып турушат. Мисалга: Ж.Баласагын атындагы КУУда болуп өткөн «Ч.Айтматов феномени», БГУда өткөн «Окутуунун инновациялык ыкмалары түркүн тилде», К.И.Скрябин атындагы КАУда болгон «Айтматовдук 5-окуу», «Актерлук чеберчилик», «Чечендик» аттуу ж.б., И.Арабаев атындагы КМУда, КМДМСАда өткөн көптөгөн Эл аралык конференцияларда баалуу баяндамалар жана макалалары менен катышып, эмгектери жарык көрдү. Кафедранын окутуучулары КЭУнун өзүндө болуп турган тренинг, семинарлардын активдүү катышуучулары.
   </DIV>


