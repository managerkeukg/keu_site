    <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Мамлекеттик жана расмий тилдер кафедрасы гуманитардык дисциплиналарды тейлөө секциясынан бөлүнүп, өз алдынча кафедра болуп түзүлгөн. Кафедранын курамында кыргыз, орус, англис, немис, француз тилдери боюнча окутуучулар эмгектенген. 1998-жылдын 18-февралынан тартып, №10/1 буйругунун негизинде биринчи кафедра башчысы болуп Айнагуль Дүйшөмбиевна Калчаева дайындалып, 2000-жылга чейин иштеп келген. Ал чыгармачылык өргүүгө кеткендигине байланыштуу кафедранын ага окутуучулары Алиясова Г.Д., кийин Узбекова Г.А. кафедраны жетектешкен. Филология илиминин доктору Жапарова Б.Б. 2000-2002-жылга чейин жетектеген учурда кафедра жана университет үчүн чоң иш жасалган. Анын жетекчилигинде «Мамлекеттик тилди окутуунун маселелери жана экономика багытындагы жогорку окуу жайларында кыргыз тилинде окутууга өтүүнүн зарылчылыгы» аттуу республикалык илимий-методикалык конференция уюштурулуп, жыйынтыгында илимий-методикалык макалалар жыйнагы жарыяланган.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
       2002-2008-жылдары кафедраны ага окутуучу Убукеева А.Т. башкарган. Ал мезгилдерде Эл аралык «Академиялык чынчылдык» аттуу долбоордун алкагында университеттин окутуучулары менен мамлекеттик жана расмий тилдер кафедрасынын окутуучулук курамы бүт бойдон «Окуу жана жазуу аркылуу сынчыл ой жүгүртүүнү өнүктүрүү» боюнча дасыгын жогорулатышып, бардыгы сертификатка ээ болушкан. 2005-жылы Мамлекеттик тил кабинети ачылып, заманбап табитте жабдууланган. КРнын Президентине караштуу мамлекеттик тил боюнча улуттук комиссиясынын төрагасы Т.Р.Жумагулов кабинеттин ачылышына келип катышып, баалуу китептерди белекке тапшырган. 
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Кафедраны 2008-жылдын сентябрь айынан тартып, азыркы учурга чейин педагогика илимдеринин кандидаты, доцент Н.Ж.Жапаралиева жетектейт.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Мамлекеттик жана расмий тилдер кафедрасында жалпысынан 10 окутуучу эмгектенет. Кафедрада 3 доцент, 2 доценттин милдетин аткаруучу, 5 ага окутуучу, анын ичинде 2 илимдин кандидаты - Жапаралиева Назира Жакыповна жана Мырзадаирова Динара Шадыбековна. Кафедранын ага окутуучусу Ашырова Н.С. илимий иштин үстүндө эмгектенүүдө. Окуу жайда 20 жылдан ашык эмгектенгендер - Узбекова Г.А.( 2003-жылдан учурга чейин Лицейдин директору), Убукеева А.Т., Алиясова Г.Д.
   </DIV>


