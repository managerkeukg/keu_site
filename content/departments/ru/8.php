   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Убукеева Айнагуль Тейишовна – Кыргыз Республикасынын билим берүү отличниги, Кыргыз Республикасынын билим берүү министрлигинин, Кыргыз Республикасынын Президентине караштуу мамлекеттик тил боюнча Улуттук комиссиясынын Ардак грамоталары менен сыйланган.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Убукеева А.Т. окуу жайдын ишине, өнүгүүсүнө кошкон салымы үчүн КЭУнун 60-жылдыгына карата медаль жана көптөгөн грамоталар менен сыйланган. Андан тышкары Айнагуль Тейишовна эл аралык проекттерге катышып, бай тажрыйбасына ээ болгон. 2009-жылдан тартып, Кыргыз Республикасынын билим берүү министрлигинин Окуу-методикалык бөлүмүнүн (экономика жана башкаруу тармагы боюнча билим берүү бөлүгү) жооптуу катчысы жана билим берүү программаларын аккредитациялоо боюнча эксперти болуп иштейт. 
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Алиясова Гүлмира Джусуповна - Кыргыз Республикасынын билим берүү министрлигинин Ардак грамотасы (2000), КРдин мамлекеттик тилин өнүктүрүүгө кошкон зор салымы үчүн Кыргыз Республикасынын Президентине караштуу мамлекеттик тил боюнча Улуттук комиссиясынын Ардак грамотасы(2005) жана КЭУнун грамоталары менен сыйланган.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Кыргыз Республикасынын билим берүү отличниги (2007). КЭУнун 60-жылдыгына карата юбилейлик медаль менен сыйланган (2013). Квалификацияны жогорулатуу курстарынын, КЭУ жана башка жогорку окуу жайларында уюштурулган көптөгөн окуу-усулдук семинар-тренингдер боюнча сертификаттарга ээ болгон. Алиясова Г.Д. 2006- жылдан бери чыккан “КЭУнун кабарлары” аттуу университеттин илимий-практикалык журналынын редактору.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Мамлекеттик жана расмий кафедрасында ийгиликтүү эмгектенген жогорку квалификациялуу, тажрыйбалуу окутуучулар: Калыбекова А.М.,Ахметова С.Е., Каримова Б.А., Адылова Б.К, Ашырова Н.С. 
