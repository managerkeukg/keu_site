   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        2008-жылы «Манастаануу» сабагын киргизүү маселеси коюлуп, «Манастаануу» курсу боюнча окуу программасы жана методикалык колдонмо» аттуу эмгек Н.Ж.Жапаралиеванын авторлугунда иштелип чыгып, анын негизинде бардык багытта билим алып жаткан студенттерге окутулуп келе жатат.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        “Манастаануу” сабагынын маанисин арттыруу үчүн белгилүү манасчылар Бакчиев Талантаалы, Исаков Рыспай, Дөөлөт Садыков, Замир Баялиев, Улан Исмаилов, Самат Көчөрбаев менен байма-бай жолугушуулар өткөрүлүп турат.
   </DIV>

    <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Мамлекеттик жана расмий тилдер кафедрасы 2012-жылдын 15-майында республикалык ЖОЖдордун арасында «Манас эпосунун улуттук жана дүйнөлүк маданиятта алган орду» аттуу илимий-практикалык студенттик конференцияны эң жогорку деңгээлде өткөрүп, коомчулуктун омоктуу ойлоруна ээ болду.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Ар бир окуу жылында Мамлекеттик тил майрамын белгилөө максатында Мамлекеттик жана расмий тилдер кафедрасы тарабынан “Эне тилим - элим, энем, уюткум”, “Кыргыз тили кылымдарды карыткан, эч ким аны өчүрө албайт тарыхтан”, “Эне тилдин ээси - эл” жана февраль айында белгиленүүчү “Эл аралык эне тил күнүнө” арналган ачык сабактар, дилбаян жана дубал гезиттердин сынактары өткөрүлүп, көптөгөн у.с. иш-чаралар салттуу түрдө белгиленет.
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Ч.Айтматовдун туулган күнүнө карата “Айтматов жана аалам” аттуу студенттик илимий-практикалык конференция, “Чыңгыз Айтматов - кыргыз элинин сыймыгы” аттуу темадагы иш-чаралар, ошондой эле кыргыз поэзиясынын классиги А.Осмоновдун чыгармачылыгына арналган дубал гезиттердин, ыр түрмөктөрүнүн, эссе, дилбаяндардын сынактары, республикабыздагы белгилүү акын-жазуучулар менен болгон жолугушуулар, тегерек столдор, студенттер арасында «Акыл ордо» аттуу интеллектуалдык акыл-таймаштар жана олимпиадалар өткөрүлүп турат. 
   </DIV>
   <DIV>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
        Кыргыз-Түрк “Манас” университети жана республикалык “Манас” жаштар ордосу” тарабынан уюштурулган “Чечен айтыш” сөз өнөрү таймашында, КРнын Президентине караштуу мамлекеттик тил боюнча улуттук комиссиясынын “Сен сүйгөн кыргыз макалы” сынагында, “Ыйык Ата-Журт” коомдук фондунун “Ата-энем – ырысым, ата-энем – бакытым” республикалык сынагында, университеттер аралык “Кеменгер” студенттик клубунун Бишкек шаарындагы ЖОЖдор арасындагы “Акыл таймашы-2013” интеллектуалдык оюнунда КЭУнун студенттери финалга чыгышып, баалуу сыйлыктардын ээлери болушкан.
   </DIV>


